class MyArray {
	public static void main(String args[]) {
		int mArr[][] = {{1, 2}, {3, 4}};

		System.out.println("mArr[0][0] = " + mArr[0][0]);
		System.out.println("mArr[0][1] = " + mArr[0][1]);
		System.out.println("mArr[1][0] = " + mArr[1][0]);
		System.out.println("mArr[1][1] = " + mArr[1][1]);
	}
}